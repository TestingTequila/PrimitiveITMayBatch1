package variables;

public class FloatingPointFamilyVariable
{
    public static void main(String[] args) {

        // 4 bytes = 4*8 =32 bits
        // Range =up to 5 digit decimal places
        float f1=122.34565464f;
        System.out.println(f1);


        // 8 bytes = 8*8 =64 bits
        double d1 = 122.5443534534;
        System.out.println(d1);
    }
}
